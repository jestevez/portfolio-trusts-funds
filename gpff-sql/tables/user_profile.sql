CREATE TABLE  GPSQLWEB.USER_PROFILE (
    ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR(128) NOT NULL UNIQUE,
    DOCUMENT_NUMBER VARCHAR(10),
    FIRST_NAME VARCHAR(128),
    SECOND_NAME VARCHAR(128), 
    LAST_NAME VARCHAR(128),
    SECOND_LAST_NAME VARCHAR(128),
    GENDER VARCHAR(1),
    CIVIL_STATUS VARCHAR(1),
    EMAIL VARCHAR(128),
    PHONE VARCHAR(20),
    MOBILE VARCHAR(20),
    BIRTHDAY DATE,
    ENABLED VARCHAR(1),
    DELETED VARCHAR(1),
    CREATED_AT TIMESTAMP,
    CREATED_BY VARCHAR(128),
    UPDATED_AT TIMESTAMP,
    UPDATED_BY VARCHAR(128),
    CONSTRAINT GPSQLWEB.PK_USER_PROFILE_ID PRIMARY KEY (ID)
)
GO



CREATE TABLE  `tesoreria`.`userProfile`
(
    `id` int(11) NOT NULL AUTO_INCREMENT COMMENT 'ID',
    `username` varchar(128) NOT NULL  COMMENT 'USERNAME',
    `documentNumber` varchar(10) NOT NULL  COMMENT 'DOCUMENT_NUMBER',
    `firstName` varchar(128) NOT NULL  COMMENT 'FIRST_NAME',
    `secondName` varchar(128) NOT NULL  COMMENT 'SECOND_NAME',
    `lastName` varchar(128) NOT NULL  COMMENT 'LAST_NAME',
    `secondLastName` varchar(128) NOT NULL  COMMENT 'SECOND_LAST_NAME',
    `gender` varchar(1) NOT NULL  COMMENT 'GENDER',
    `civilStatus` varchar(1) NOT NULL  COMMENT 'CIVIL_STATUS',
    `email` varchar(128) NOT NULL  COMMENT 'EMAIL',
    `phone` varchar(20) NOT NULL  COMMENT 'PHONE',
    `mobile` varchar(20) NOT NULL  COMMENT 'MOBILE',
    `brithday` date NOT NULL  COMMENT 'BIRTHDAY',
    `enabled` varchar(1) NOT NULL  COMMENT 'ENABLED',
    `deleted` varchar(1) NOT NULL  COMMENT 'DELETED',
    `createdAt` TIMESTAMP NOT NULL  COMMENT 'CREATED_AT',
    `createdBy` varchar(128) NOT NULL  COMMENT 'CREATED_BY',
    `updatedAt` TIMESTAMP NOT NULL  COMMENT 'UPDATED_AT',
    `updatedBy` varchar(128) NOT NULL  COMMENT 'UPDATED_BY',
    PRIMARY KEY (`id`),
    UNIQUE (`username`)  
