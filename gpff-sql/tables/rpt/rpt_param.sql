DROP TABLE gpsqlweb.data_source
GO

CREATE TABLE  gpsqlweb.param (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    param_type_id BIGINT DEFAULT NULL,
    report_id BIGINT DEFAULT NULL,
    name varchar(255) NOT NULL,
    default_value varchar(255) DEFAULT NULL,
    position BIGINT NOT NULL,
    required char(1) DEFAULT NULL,
    enabled char(1) DEFAULT 'Y',
    deleted char(1) DEFAULT 'N',
    created_at timestamp,
    created_by VARCHAR(20),
    updated_at timestamp,
    updated_by VARCHAR(20),
    CONSTRAINT gpsqlweb.pk_data_source_id PRIMARY KEY (id),
    FOREIGN KEY (param_type_id) REFERENCES param_type(id),
    FOREIGN KEY (report_id) REFERENCES report(id)
)
GO 

