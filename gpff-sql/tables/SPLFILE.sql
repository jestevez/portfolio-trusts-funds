CREATE TABLE SPLFILE (
       ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
       SPLDATA BLOB,
       SPLNAME VARCHAR(255) NOT NULL,
       SPLMIME VARCHAR(100) NOT NULL,
       SPLDATE TIMESTAMP NOT NULL,
       SPLTYPE VARCHAR(2) NOT NULL,
       SPLUSER VARCHAR(10) NOT NULL,
       SPLSTATUS VARCHAR(2) NOT NULL,
       CONSTRAINT PK_SPLFILE_ID PRIMARY KEY (ID)
)
GO

